  function safeLang(lang) {
    return SUPPORTED.includes(lang) ? lang : 'es';
  }

  function t(key, lang) {
    const current = safeLang(lang || getLanguage());
    return I18N[current][key] || I18N.es[key] || key;
  }

  function tagTranslatables() {
    SELECTORS.forEach(([selector, key, type]) => {
      const el = document.querySelector(selector);
      if (!el) return;
      if (!el.dataset.i18n) el.dataset.i18n = key;
      if (type && !el.dataset.i18nType) el.dataset.i18nType = type;
    });

    PLACEHOLDER_SELECTORS.forEach(([selector, key]) => {
      const el = document.querySelector(selector);
      if (!el) return;
      el.dataset.i18nPlaceholder = key;
    });
  }

  function apply(lang) {
    const next = safeLang(lang);
    document.documentElement.setAttribute('lang', next);
    document.documentElement.setAttribute('data-lang', next);

    document.querySelectorAll('[data-i18n]').forEach((el) => {
      const key = el.dataset.i18n;
      const type = el.dataset.i18nType || 'text';
      const value = t(key, next);
      if (type === 'html') {
        el.innerHTML = value;
      } else if (el.children.length) {
        let updated = false;
        const childNodes = Array.from(el.childNodes);
        childNodes.forEach((node) => {
          if (updated) return;
          if (node.nodeType === Node.TEXT_NODE && node.nodeValue.trim()) {
            node.nodeValue = ' ' + value + ' ';
            updated = true;
          }
        });
        if (!updated) el.insertBefore(document.createTextNode(value + ' '), el.firstChild);
      } else {
        el.textContent = value;
      }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
      el.setAttribute('placeholder', t(el.dataset.i18nPlaceholder, next));
    });
  }

  function getLanguage() {
    return safeLang(localStorage.getItem(LANG_KEY) || document.documentElement.getAttribute('data-lang') || 'es');
  }

  function setLanguage(lang) {
    const next = safeLang(lang);
    localStorage.setItem(LANG_KEY, next);
    apply(next);
    document.dispatchEvent(new CustomEvent('ecoclima:langchange', { detail: { lang: next } }));
  }

  tagTranslatables();
  apply(getLanguage());

  window.EcoClimaI18n = {
    t,
    getLanguage,
    setLanguage
  };
